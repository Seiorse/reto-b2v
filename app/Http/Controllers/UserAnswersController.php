    {
        
        $validData = $request->validate(
                            [
                                'tags' => 'required'
                            ]
                          );

        foreach ($request->tags as $tags) 
        {
            $userAnswers = new UserAnswers();

            $userAnswers->user_id = auth()->id();
            $userAnswers->answer_id = (int)$tags;

            $userAnswers->save();
        }

        return redirect('/start');

    }

